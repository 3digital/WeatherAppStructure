//an example of a class that will hold weather data

public class AwesomeWeatherData {

    private double mWindspeed;
    private double mWindDirection;
    private double mWindLikenessToRobertDuval;

    public double getmWindLikenessToRobertDuval() {
        return mWindLikenessToRobertDuval;
    }

    public void setmWindLikenessToRobertDuval(double mWindLikenessToRobertDuval) {
        this.mWindLikenessToRobertDuval = mWindLikenessToRobertDuval;
    }

    public double getmWindspeed() {
        return mWindspeed;
    }

    public void setmWindspeed(double mWindspeed) {
        this.mWindspeed = mWindspeed;
    }

    public double getmWindDirection() {
        return mWindDirection;
    }

    public void setmWindDirection(double mWindDirection) {
        this.mWindDirection = mWindDirection;
    }



}
